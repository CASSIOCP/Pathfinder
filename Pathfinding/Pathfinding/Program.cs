        }

        static void FindPath()
        {
            if (Search(0, 0, new Vertex() { Cost = 0 }))
            {
                var destinationNode = Verts.FirstOrDefault(a => a.Value.IsDestination);
                while (destinationNode.Value.Cost >= 0)
                {
                    var printn = destinationNode.Value.IsDestination ? 9 : 1;
                    if (destinationNode.Key < cols)
                        solution[0, destinationNode.Key] = printn;
                    else
                        solution[(destinationNode.Key / cols), destinationNode.Key % cols] = printn;

                    if (destinationNode.Value.Cost == 0)
                        break;

                    destinationNode = Verts.FirstOrDefault(a => a.Key == destinationNode.Value.Source);
                }
                Console.WriteLine($"Grid: {rows} x {cols}");
                Console.WriteLine($"Counter: {counter}");

                Console.WriteLine("Path:");
                for (int i = 0; i < rows; i++)
                {
                    for (int j = 0; j < cols; j++)
                    {
                        Console.Write($"{solution[i, j]} ");
                    }
                    Console.WriteLine();
                }

                Console.WriteLine("Steps: " + Verts.FirstOrDefault(a => a.Value.IsDestination).Value.Cost);
            }
            else
            {
                Console.Write("Not found!");
            }
        }

        static bool Search(int row, int col, Vertex vertex)
        {
            counter++;
            if (row < 0 || col < 0 || row >= rows || col >= cols || matrix[row, col] == 0)
            {
                return false;
            }

            int key = (row * cols) + col;

            if (Verts.ContainsKey(key) && Verts[key].Cost <= vertex.Cost)
            {
                return false;
            }

            Verts[key] = vertex;

            if (matrix[row, col] == 9)
            {
                Verts[key].IsDestination = true;
                return true;
            }

            Vertex v = new Vertex()
            {
                Cost = vertex.Cost + 1,
                Source = key,
            };

            bool hasdestination = false;

            if (Search(row + 1, col, v))
                hasdestination = true;

            if (Search(row, col + 1, v))
                hasdestination = true;

            if (Search(row - 1, col, v))
                hasdestination = true;

            if (Search(row, col - 1, v))
                hasdestination = true;

            return hasdestination;
        }
