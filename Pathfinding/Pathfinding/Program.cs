        static int rows;
        static int counter;
        static Dictionary<int, Vertex> Verts = new Dictionary<int, Vertex>();

        static void Main(string[] args)
        {
            rows = matrix.GetLength(0);
            cols = matrix.GetLength(1);
            solution = new int[rows, cols];

            FindPath();
